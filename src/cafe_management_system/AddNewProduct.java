    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        ArrayList<Category> list = CategoryDao.getAllRecords();
        Iterator<Category> itr = list.iterator();
        while(itr.hasNext()){
            Category categoryObj = itr.next();
            textCategory.addItem(categoryObj.getName());
        }
        
        
    }//GEN-LAST:event_formComponentShown

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        Product product = new Product();
        product.setName(textName.getText());
        product.setCategory((String)textCategory.getSelectedItem());
        product.setPrice(textPrice.getText());
        product.setCat_Id(CategoryDao.get_cat_id((String)textCategory.getSelectedItem()));
        ProductDao.save(product);
        setVisible(false);
        new AddNewProduct().setVisible(true);
        
    }//GEN-LAST:event_btnSaveActionPerformed

